import type { Hop, IncidentData, TimelineEvent, TrackedWallet } from "./types"

const REKT_SOURCE = "Rekt News incident writeup"

type ExpandedConfig = {
  id: string
  name: string
  victim: string
  attribution: string
  rootCause: string
  loss: number
  start: number
  pause?: number
  chains: string[]
  stats: IncidentData["incident"]["stats"]
  hops: Hop[]
  timeline: TimelineEvent[]
  wallets?: TrackedWallet[]
}

function incident(config: ExpandedConfig): IncidentData {
  return {
    incident: {
      id: config.id,
      name: config.name,
      victim: config.victim,
      attacker_attribution: config.attribution,
      root_cause: config.rootCause,
      loss_usd: config.loss,
      start_ts: config.start,
      pause_ts: config.pause,
      chains_touched: config.chains,
      stats: config.stats,
    },
    hops: config.hops,
    timeline: config.timeline,
    tracked_wallets: config.wallets ?? [],
  }
}

function watch(
  address: string,
  chain: string,
  label: string,
  role: string,
  notes: string
): TrackedWallet {
  return {
    address,
    chain,
    label,
    role,
    confidence: "verified",
    sourceLabel: REKT_SOURCE,
    notes,
  }
}

